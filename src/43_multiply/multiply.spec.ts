import { multiply, multiply1 } from "./multiply";

test("multiply", () => {
  expect(multiply("123", "111")).toBe("13653");
  expect(multiply("1234", "567")).toBe("699678");
  expect(multiply("43179", "126374")).toBe("5456702946");
  expect(multiply("221714711256524754", "35781375725626")).toBe(
    "7933257387368392489883781146004"
  );
  expect(
    multiply("6923875646254986295615657348", "7136574235629357632753497569")
  ).toBe("49412752547764903423775425003037786998535393106054987012");
});

